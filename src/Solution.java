import java.util.Scanner;

public class Solution {
    public static void main(String[] args) {
        Scanner scanner = new Scanner(System.in);

        // N을 입력하세요
        // System.out.println("N을 입력하세요:"); // 주석 처리: 일반적으로 자동 채점 시스템에서는 프롬프트가 필요 없음
        int n = scanner.nextInt();

        // M을 입력하세요
        // System.out.println("M을 입력하세요:"); // 주석 처리
        int m = scanner.nextInt();

        int k;

        // 문제의 조건에서 N개의 버튼 중 M개의 버튼이 전원을 켤 수 있다고 했으므로,
        // 일반적으로 N >= M >= 1 이라고 가정할 수 있습니다.

        // 경우 1: 버튼이 총 1개인 경우 (N=1)
        // 이 경우 M도 반드시 1이어야 합니다 (N >= M >= 1 이므로).
        // 전원을 켤 수 있는 버튼 1개를 찾기 위해서는 해당 버튼을 눌러봐야 합니다.
        // 따라서 누름 횟수는 1회입니다.
        if (n == 1) {
            k = 1;
        }
        // 경우 2: 버튼이 2개 이상인 경우 (N > 1)
        else {
            // 최악의 경우, K의 최댓값은 N-1 입니다.
            // N-1개의 버튼을 눌러보면, 마지막 N번째 버튼의 상태는 누르지 않고도 알 수 있습니다.
            //
            // 상세 설명:
            // 우리는 M개의 'O' (전원 켜짐) 버튼을 찾거나, N-M개의 'X' (전원 안 켜짐) 버튼을 찾으면 멈춥니다.
            // (N-M개의 'X' 버튼을 찾으면 나머지 M개는 'O'임이 확실해지기 때문입니다.)
            //
            // 최악의 시나리오는 M-1개의 'O' 버튼과 (N-M)-1개의 'X' 버튼을 식별했을 때입니다.
            // 이때까지 누른 횟수는 (M-1) + (N-M-1) = N-2 회입니다.
            // 이제 남은 버튼은 2개이며, 하나는 우리가 찾던 M번째 'O' 버튼이고, 다른 하나는 (N-M)번째 'X' 버튼입니다.
            // 여기서 한 번 더 누르면 (총 N-2+1 = N-1회 누름):
            //   - 만약 'O' 버튼을 눌렀다면: M개의 'O' 버튼을 모두 찾았으므로 멈춥니다.
            //   - 만약 'X' 버튼을 눌렀다면: N-M개의 'X' 버튼을 모두 찾았으므로 멈춥니다.
            // 이 논리는 M >= 1 이고 N-M >= 1 (즉, M <= N-1) 일 때 유효합니다.
            //
            // M=N인 경우 (모든 버튼이 'O'인 경우, N > 1):
            // N-1개의 버튼을 누르면 모두 'O'일 것입니다. (N-1회 누름)
            // 마지막 N번째 버튼은 누르지 않아도 'O'임을 알 수 있습니다.
            // 따라서 K = N-1 입니다.
            //
            // 결론적으로 N > 1인 모든 경우에 대해, 최악의 경우 필요한 최대 누름 횟수는 N-1 입니다.
            // 예제: N=4, M=2. K = 4-1 = 3. (문제의 예제 해설과 일치)
            k = n - 1;
        }

        System.out.println(k);

        scanner.close();
    }
}